

module inline_constraint;
 class dynamic_array;
   rand logic [7:0] arr [];
   
   constraint it{foreach(arr[i]) arr[i]<70;}
   
   constraint s{arr.sum()  with (int'(item))==100;}
   
   constraint m{
     arr.size()<10;
     arr.size()>3;}
 endclass
  
  dynamic_array hi;
  initial begin 
    hi=new();
    $display("=====================================");
    $display("    Inline with Class CONSTRAINT");
    $display("=====================================");
    repeat(20)begin
      hi.randomize() with {foreach(arr[i]) arr[i]>10;
                          arr.size==6;
                          arr.sum()  with (int'(item))==100;};
    
      $display("Size  : %0d",hi.arr.size());
      $display("Array : %0p",hi.arr);
      $display("---------------------------------------");
  end
  end 
endmodule

