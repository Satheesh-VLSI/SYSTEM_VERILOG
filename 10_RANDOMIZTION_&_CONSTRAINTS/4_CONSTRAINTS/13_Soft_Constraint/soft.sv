
module soft_constraint;
 class dynamic_array;
   rand logic [7:0] arr [];
   
   constraint it{  foreach(arr[i]) soft arr[i]>10;
                      soft arr.sum()  with (int'(item))==100;}
   //without this soft this class constraint and inline constrain would face conflict
   constraint m{
     soft arr.size()<6;
     arr.size()>3;}
 endclass
  
  dynamic_array hi;
  initial begin 
    hi=new();
    $display("=====================================");
    $display("         SOFT CONSTRAINT");
    $display("=====================================");
    repeat(20)begin
      if(hi.randomize() with {foreach(arr[i]) arr[i]>15;
                              arr.size==7;
                              arr.sum()  with (int'(item))==200;})
        begin
    
      $display("Size  : %0d",hi.arr.size());
        $display("Array : %0p",hi.arr);
      $display("---------------------------------------");
        end
      else begin
        $display("    Randomiztion failed      ");
      $display("---------------------------------------");
      end
  end
  end 
endmodule


